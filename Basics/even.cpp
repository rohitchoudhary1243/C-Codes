#include <iostream>
using namespace std;
int main()
{
    int a, b, c;
    cin >> a >> b >> c;
    if (a % 2 == 0)
    {
        cout << "A is even number" << endl;
    }
    else
    {
        cout << "A is odd number" << endl;
    }
    if (b % 2 == 0)
    {
        cout << "B is even number" << endl;
    }
    else
    {
        cout << "B is odd number" << endl;
    }
    if (c % 2 == 0)
    {
        cout << "C is even number" << endl;
    }
    else
    {
        cout << "C is odd number" << endl;
    }
}